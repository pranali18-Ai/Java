public class variable_define {
    public static void main(String[] args) {
    int apples = 5;
    int oranges = 3;
    String name = "strawberry";
    System.out.println(apples);
    System.out.println(oranges);
    System.out.println(name);

    apples = oranges; 
    System.out.println(apples);
    }   
}
